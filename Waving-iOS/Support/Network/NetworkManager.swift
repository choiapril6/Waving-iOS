import Foundation

final class NetworkManager {
    
    static let shared = NetworkManager()
    
    static let domainUrlString = "http://118.67.130.127:8080"
    
    public func userAuthentication(completionHandler: (() -> Void)? = nil) {
        let url = URL(string: NetworkManager.domainUrlString + "/v1/users/authentication")!
        var request = URLRequest(url: url)
        request.setValue("application/json", forHTTPHeaderField: "Content-Type")
        request.setValue("application/json", forHTTPHeaderField: "Accept")
        request.httpMethod = "POST"
        let parameters: [String: String] = [
            "cellphone": "01020176384"
        ]
        request.httpBody = try! JSONEncoder().encode(parameters)
        
        let task = URLSession.shared.dataTask(with: request) { data, response, error in
            guard
                let data = data,
                let response = response as? HTTPURLResponse,
                error == nil
            else {                                                               // check for fundamental networking error
                print("error", error ?? URLError(.badServerResponse))
                return
            }
            
            guard (200 ... 299) ~= response.statusCode else {                    // check for http errors
                print("statusCode should be 2xx, but is \(response.statusCode)")
                print("response = \(response)")
                return
            }
            
            // do whatever you want with the `data`, e.g.:
            
            do {
                let json = String(data: data, encoding: .utf16)
                print("Response: \(json)")
//                let responseObject = try JSONDecoder().decode(ResponseObject<Foo>.self, from: data)
//                print(responseObject)
            } catch {
                print(error) // parsing error
                
                if let responseString = String(data: data, encoding: .utf8) {
                    print("responseString = \(responseString)")
                } else {
                    print("unable to parse response as string")
                }
            }
        }
        task.resume()
    }

